    private void jButton_EliminarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EliminarPedidoActionPerformed
        // TODO add your handling code here:
    int selectedRow = jTable_Pedido.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un pedido para eliminar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        NumeroPedidos.remove(selectedRow);
        Nombres.remove(selectedRow);
        Apellidos.remove(selectedRow);
        Ingredientes.remove(selectedRow);
        Tamanos.remove(selectedRow);
        Totales.remove(selectedRow);

        actualizarTabla();
        actualizarTotal();
        
    }//GEN-LAST:event_jButton_EliminarPedidoActionPerformed

