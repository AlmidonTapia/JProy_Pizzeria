    private void jButton_AgregarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_AgregarPedidoActionPerformed
        String NPedido = String.valueOf(pedidoCounter); // Asignamos el valor del contador
        String Nombre = jTextField_InputNombre.getText();
        String Apellido = jTextField_InputApellido.getText();
        String Ingrediente = "";
        String TamanoPizza = "";
        Double Total = 0.0;

        if (Nombre.isEmpty() || Apellido.isEmpty() || (!jRadioButton_Tamaño1.isSelected() && !jRadioButton_Tamaño2.isSelected() && !jRadioButton_Tamaño3.isSelected())) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (jCheckBox_Ingrediente1.isSelected()) {
            Ingrediente += "Bacon ";
            Total += 1.5;
        }
        if (jCheckBox_Ingrediente2.isSelected()) {
            Ingrediente += "Anchoas ";
            Total += 1.80;
        }
        if (jCheckBox_Ingrediente3.isSelected()) {
            Ingrediente += "Cebolla ";
            Total += 1.0;
        }
        if (jCheckBox_Ingrediente4.isSelected()) {
            Ingrediente += "Pimenton ";
            Total += 1.2;
        }

        if (jRadioButton_Tamaño1.isSelected()) {
            TamanoPizza = "Pequeño";
            Total += 15.0;
        } else if (jRadioButton_Tamaño2.isSelected()) {
            TamanoPizza = "Mediano";
            Total += 30.0;
        } else if (jRadioButton_Tamaño3.isSelected()) {
            TamanoPizza = "Familiar";
            Total += 45.0;
        }

        NumeroPedidos.add(NPedido);
        Nombres.add(Nombre);
        Apellidos.add(Apellido);
        Ingredientes.add(Ingrediente.trim());
        Tamanos.add(TamanoPizza);
        Totales.add(Total);
        
        pedidoCounter++; // Incrementamos el contador de pedidos para el siguiente
       // 
        actualizarTabla();
        actualizarTotal();
    }//GEN-LAST:event_jButton_AgregarPedidoActionPerformed

    private void actualizarTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable_Pedido.getModel();
        model.setRowCount(0); 

        for (int i = 0; i < NumeroPedidos.size(); i++) {
            model.addRow(new Object[]{
                NumeroPedidos.get(i),
                Nombres.get(i),
                Apellidos.get(i),
                Ingredientes.get(i),
                Tamanos.get(i),
                Totales.get(i)
            });
        }
    }

    private void actualizarTotal() {
        double totalGeneral = 0.0;
        for (Double total : Totales) {
            totalGeneral += total;
        }
        jLabel_MontoTotal.setText(String.valueOf(totalGeneral));
    }
    

